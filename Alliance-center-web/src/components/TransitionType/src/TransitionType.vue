<template>
  <transition
    v-bind="$attrs"
    :enter-active-class="`animated faster ${transitionClass[0]}`"
    :leave-active-class="`animated faster ${transitionClass[1]}`"
    :mode="mode"
  >
    <div style="height:100%">
      <slot></slot>
    </div>
  </transition>
</template>

<script>
export default {
  name: 'transition-type',
  props: ['name', 'mode'],
  data() {
    return {
      transitionClass: ['fadeInRight', 'fadeOutRight']
    }
  },
  watch: {
    name: {
      immediate: true,
      handler(val) {
        if (!val) {
          return
        }
        this.transitionClass = val.split('-')
      }
    }
  }
}
</script>

